    container: { flex: 1, backgroundColor: theme.background },
    scrollArea: { flex: 1, paddingHorizontal: 20 },
    header: { paddingTop: 20, paddingBottom: 15 },
    systemText: { color: theme.textMuted, fontSize: 10, fontWeight: 'bold', letterSpacing: 2, marginBottom: 4 },
    title: { color: theme.textMain, fontSize: 36, fontWeight: 'bold' },
    badgeRow: { flexDirection: 'row', marginTop: 10, alignItems: 'center' },
    badge: { backgroundColor: 'rgba(255,255,255,0.1)', paddingHorizontal: 12, paddingVertical: 6, borderRadius: 8, flexDirection: 'row', alignItems: 'center' },
